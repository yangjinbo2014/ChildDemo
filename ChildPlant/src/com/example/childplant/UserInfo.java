package com.example.childplant;

public class UserInfo {
	private String userName;
	private int userBeanCount;
	
	public UserInfo(String name, int beanCount) {
		name = userName;
		userBeanCount = beanCount;
	}
	
	public String getName(){
		return userName;
	}
	
	public int getBeanCount(){
		return userBeanCount;
	}
}
